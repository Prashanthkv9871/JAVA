public class LogicalOperator{
    public static void main(String[] args){
        int value1 = 1;
        int value2 = 2;

        if((value1 == 1) && (value2 == 2)){
            System.out.println("Value is 1 AND Value is 2");
        }

        if((value1 == 1) || (value2 == 2)){
            System.out.println("Value is 1 OR value is 2");
        }
    }
}